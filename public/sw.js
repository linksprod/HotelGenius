// Hotel Genius Service Worker
const CACHE_NAME = 'hotel-genius-v2';
const STATIC_ASSETS = [
    '/',
    '/index.html',
];

// URLs that should never be cached (always fetched fresh)
const NEVER_CACHE = [
    '/manifest.json',  // The static manifest is replaced dynamically — keep it fresh
];

self.addEventListener('install', (event) => {
    event.waitUntil(
        caches.open(CACHE_NAME).then((cache) => {
            return cache.addAll(STATIC_ASSETS);
        })
    );
    self.skipWaiting();
});

self.addEventListener('activate', (event) => {
    event.waitUntil(
        caches.keys().then((cacheNames) => {
            return Promise.all(
                cacheNames
                    .filter((name) => name !== CACHE_NAME)
                    .map((name) => caches.delete(name))
            );
        })
    );
    self.clients.claim();
});

self.addEventListener('fetch', (event) => {
    // Only handle GET requests
    if (event.request.method !== 'GET') return;

    const url = event.request.url;

    // Skip blob: URLs (used for dynamic manifests generated by useHotelBranding)
    if (url.startsWith('blob:')) return;

    // Skip cross-origin requests (e.g. Supabase API calls)
    if (!url.startsWith(self.location.origin)) return;

    // Skip never-cache list — always fetch from network
    const pathname = new URL(url).pathname;
    if (NEVER_CACHE.some((p) => pathname === p || pathname.startsWith(p))) {
        event.respondWith(fetch(event.request));
        return;
    }

    event.respondWith(
        fetch(event.request)
            .then((response) => {
                // Cache successful responses
                if (response && response.status === 200) {
                    const responseClone = response.clone();
                    caches.open(CACHE_NAME).then((cache) => {
                        cache.put(event.request, responseClone);
                    });
                }
                return response;
            })
            .catch(() => {
                // Fallback to cache when offline
                return caches.match(event.request).then((cached) => {
                    return cached || caches.match('/index.html');
                });
            })
    );
});
